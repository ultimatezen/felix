class CClientRect : public CRect
{
public:
	CClientRect (TWindow window)
	{
		ATLVERIFY(window.GetClientRect(this)) ;
	}
};


///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
class CWindowRect : public CRect
{
public:
    CWindowRect (TWindow window)
    {
		ATLVERIFY(window.GetWindowRect(this)) ;
    }
};